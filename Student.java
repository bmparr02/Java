
package cecs220_a3_p5;

public class Student {

    private String name;
    private double avg;

    public Student(String name, double avg)
    {
        this.name = name;
        this.avg = avg;
    }
 
    public String getName() 
    {
        return name;
    }

    public double getAvg()
    {
        return avg;
    }


}
